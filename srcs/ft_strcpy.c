#include "libft.h"
#include <stdio.h>
#include <string.h>

char	*ft_strcpy(char *dst, const char *src)
{
	size_t	i;

	i = 0;
	while (src[i])
	{
		dst[i] = src[i];
		i++;
	}
	dst[i] = '\0';
	return (dst);
}

int		main()
{
	char	str[100];
	char 	*str1 = "hello";

	printf("%s", ft_strcpy(str, str1));
	return (0);	
	
}	

