#include "libft.h"

void	ft_putnbr(int nb)
{
	char	c[11];
	int		l;

	l = 0;
	if (nb == 0)
		ft_putchar('0');
	if (nb < 0)
	{
		ft_putchar('-');
	}
	while (l <= 11 && nb != 0)
	{
		if (nb % 10 < 0)
			c[l] = ((nb % 10) * -1) + '0';
		else
			c[l] = (nb % 10) + '0';
		nb = nb / 10;
		l++;
	}
	while (l >= 0)
	{
		ft_putchar(c[l]);
		l--;
	}
}
